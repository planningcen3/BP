function parseThaiBudgetDate(value) {
  const text = String(value || "").trim();
  if (!text) return null;

  const compact = text.replace(/\s+/g, " ").replace(/\.$/g, "");
  const monthMap = {
    "ม.ค": 0,
    "ก.พ": 1,
    "มี.ค": 2,
    "เม.ย": 3,
    "พ.ค": 4,
    "มิ.ย": 5,
    "ก.ค": 6,
    "ส.ค": 7,
    "ก.ย": 8,
    "ต.ค": 9,
    "พ.ย": 10,
    "ธ.ค": 11,
  };

  const match = compact.match(/(\d{1,2})(?:-\d{1,2})?\s+([ก-๙]+\.[ก-๙]*|\S+)\s*(\d{2})/);
  if (!match) return null;

  const day = Number(match[1]);
  const monthKey = match[2].replace(/\.+$/g, "");
  const month = monthMap[monthKey];
  if (month === undefined) return null;

  const buddhistYear = 2500 + Number(match[3]);
  const gregorianYear = buddhistYear - 543;
  return new Date(gregorianYear, month, day);
}

function normalizeBudgetDate(date) {
  if (!(date instanceof Date) || Number.isNaN(date.getTime())) return date;
  const year = date.getFullYear();
  if (year >= 1900 && year <= 1999) {
    const shifted = new Date(date);
    shifted.setFullYear(year + 57);
    return shifted;
  }
  return date;
}

function parseFlexibleDate(value) {
  if (value instanceof Date && !Number.isNaN(value.getTime())) return normalizeBudgetDate(value);

  if (typeof value === "number" && Number.isFinite(value)) {
    if (value < 20000 || value > 60000) return null;
    const excelEpoch = new Date(Date.UTC(1899, 11, 30));
    return normalizeBudgetDate(new Date(excelEpoch.getTime() + value * 86400000));
  }

  const text = String(value || "").trim();
  if (!text) return null;

  const isoDate = parseIsoDate(text);
  if (isoDate) return isoDate;

  const thaiDate = parseThaiBudgetDate(text);
  if (thaiDate) return normalizeBudgetDate(thaiDate);

  if (/^\d{4}-\d{2}-\d{2}T/.test(text)) {
    const parsed = new Date(text);
    if (!Number.isNaN(parsed.getTime())) return normalizeBudgetDate(parsed);
  }

  if (/^\d+(\.\d+)?$/.test(text)) {
    const numericValue = Number(text);
    if (numericValue < 20000 || numericValue > 60000) return null;
    const serialDate = parseFlexibleDate(numericValue);
    if (serialDate && !Number.isNaN(serialDate.getTime())) return serialDate;
  }

  return null;
}

