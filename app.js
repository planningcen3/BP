async function postToGoogleSheet(payload) {
  const scriptUrl = getScriptUrlForBudget();
  if (!hasScriptUrl()) {
    return { ok: false, skipped: true, message: `ยังไม่ได้ตั้งค่า Google Sheet ของ${activeBudgetProfile().label}` };
  }

  const body = new URLSearchParams({
    payload: JSON.stringify(payload),
  });

  await fetch(scriptUrl, {
    method: "POST",
    mode: "no-cors",
    headers: {
      "Content-Type": "application/x-www-form-urlencoded;charset=UTF-8",
    },
    body,
  });

  return { ok: true };
}

