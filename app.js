function structuredBudgetSeed(key) {
  return structuredClone(budgetProfiles[key].seedData);
}

function createDefaultBudgetsState() {
  return Object.fromEntries(
    Object.keys(budgetProfiles).map((key) => [
      key,
      {
        categories: structuredBudgetSeed(key).categories,
        transactions: structuredBudgetSeed(key).transactions,
        lastRemoteUpdate: "",
      },
    ]),
  );
}

function migrateLegacyState() {
  const saved = localStorage.getItem(legacyStorageKey);
  const budgets = createDefaultBudgetsState();
  if (!saved) {
    return {
      activeBudget: "operations",
      budgets,
    };
  }

  try {
    const parsed = JSON.parse(saved);
    budgets.operations = {
      categories: parsed.categories?.length ? parsed.categories : structuredBudgetSeed("operations").categories,
      transactions: Array.isArray(parsed.transactions) ? parsed.transactions : structuredBudgetSeed("operations").transactions,
      lastRemoteUpdate: "",
    };
  } catch {
    budgets.operations = {
      categories: structuredBudgetSeed("operations").categories,
      transactions: structuredBudgetSeed("operations").transactions,
      lastRemoteUpdate: "",
    };
  }

  return {
    activeBudget: "operations",
    budgets,
  };
}

function loadState() {
  const saved = localStorage.getItem(storageKey);
  if (!saved) return migrateLegacyState();

  try {
    const parsed = JSON.parse(saved);
    const budgets = createDefaultBudgetsState();
    for (const key of Object.keys(budgetProfiles)) {
      const incoming = parsed.budgets?.[key];
      budgets[key] = {
        categories: incoming?.categories?.length ? incoming.categories : structuredBudgetSeed(key).categories,
        transactions: Array.isArray(incoming?.transactions) ? incoming.transactions : structuredBudgetSeed(key).transactions,
        lastRemoteUpdate: incoming?.lastRemoteUpdate || "",
      };
    }
    const activeBudget = Object.prototype.hasOwnProperty.call(budgetProfiles, parsed.activeBudget) ? parsed.activeBudget : "operations";
    return { activeBudget, budgets };
  } catch {
    return migrateLegacyState();
  }
}

function saveState() {
  localStorage.setItem(storageKey, JSON.stringify(state));
}

function loadScriptUrls() {
  const defaults = Object.fromEntries(
    Object.entries(budgetProfiles).map(([key, profile]) => [key, profile.embeddedScriptUrl || ""]),
  );

  const saved = localStorage.getItem(scriptUrlKey);
  if (saved) {
    try {
      return { ...defaults, ...JSON.parse(saved) };
    } catch {
      return defaults;
    }
  }

  const legacy = localStorage.getItem(legacyScriptUrlKey);
  if (legacy) {
    return {
      ...defaults,
      operations: legacy,
    };
  }

  return defaults;
}

