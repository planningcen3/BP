function setStateFromRemote(payload) {
  if (!payload || !payload.ok) return;
  if (!Array.isArray(payload.categories) || !Array.isArray(payload.transactions)) return;

  const current = activeBudgetState();
  current.categories = payload.categories;
  current.transactions = payload.transactions;
  current.lastRemoteUpdate = payload.updatedAt || "";
  saveState();

  if (payload.updatedAt) {
    setSheetStatus("online", `Sync ล่าสุด ${new Date(payload.updatedAt).toLocaleTimeString("th-TH", { hour: "2-digit", minute: "2-digit" })}`);
  }
  renderAll();
}

function loadRemoteSnapshot() {
  return new Promise((resolve, reject) => {
    const scriptUrl = getScriptUrlForBudget();
    if (!hasScriptUrl()) {
      resolve(null);
      return;
    }

    const callbackName = `peaBudgetSync_${activeBudgetKey()}_${Date.now()}_${Math.floor(Math.random() * 1000)}`;
    const cleanup = () => {
      delete window[callbackName];
      script.remove();
    };

    window[callbackName] = (payload) => {
      cleanup();
      resolve(payload);
    };

    const script = document.createElement("script");
    script.src = `${scriptUrl}?mode=snapshot&callback=${callbackName}`;
    script.async = true;
    script.onerror = () => {
      cleanup();
      reject(new Error("Snapshot load failed"));
    };
    document.body.appendChild(script);
  });
}

async function syncFromGoogleSheet() {
  if (!hasScriptUrl()) return;
  try {
    const payload = await loadRemoteSnapshot();
    if (!payload) return;
    setStateFromRemote(payload);
  } catch {
    setSheetStatus("error", `อ่านข้อมูล ${activeBudgetProfile().label} จาก Google Sheet ไม่สำเร็จ`);
  }
}

function startAutoSync() {
  if (syncTimer) clearInterval(syncTimer);
  if (!hasScriptUrl()) return;
  syncTimer = setInterval(() => {
    syncFromGoogleSheet();
  }, syncIntervalMs);
}

