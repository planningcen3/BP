function exportCsv() {
  const header = ["id", "date", "category", "employee", "detail", "amount", "note"];
  const rows = activeTransactions().map((item) => header.map((key) => `"${String(item[key] ?? "").replaceAll('"', '""')}"`).join(","));
  const csv = `\ufeff${header.join(",")}\n${rows.join("\n")}`;
  const blob = new Blob([csv], { type: "text/csv;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = `${activeBudgetProfile().label}-transactions.csv`;
  link.click();
  URL.revokeObjectURL(url);
}

